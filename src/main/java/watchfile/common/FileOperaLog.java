package watchfile.common;

public class FileOperaLog {
	private String appName;
	private String filePath;
	private String chefileName;
	private String tarName;
	private String cuttState;
	private String carryState;
	private String createTime;
	private String updateTime;
	private String stateremark;
	
	
	public String getStateremark() {
		return stateremark;
	}
	public void setStateremark(String stateremark) {
		this.stateremark = stateremark;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getChefileName() {
		return chefileName;
	}
	public void setChefileName(String chefileName) {
		this.chefileName = chefileName;
	}
	public String getTarName() {
		return tarName;
	}
	public void setTarName(String tarName) {
		this.tarName = tarName;
	}
	public String getCuttState() {
		return cuttState;
	}
	public void setCuttState(String cuttState) {
		this.cuttState = cuttState;
	}
	public String getCarryState() {
		return carryState;
	}
	public void setCarryState(String carryState) {
		this.carryState = carryState;
	}
	
	
}
